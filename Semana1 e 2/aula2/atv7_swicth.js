let letra ='c'.toUpperCase();

switch (letra) {
    case 'A':
        console.log('Excelente');
        break;
    case 'B':
        console.log('Muito Bom');
        break;
    case 'C':
        console.log('Bom');
        break;
    case 'D':
        console.log('Regular');
        break;
    case 'E':
        console.log('Reprovado');
        break;
    default:
        console.log('Conceito inválido');
        break;
}